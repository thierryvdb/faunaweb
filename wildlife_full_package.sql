  time_out_hours    NUMERIC(8,2) CHECK (time_out_hours IS NULL OR time_out_hours >= 0),
  cost_brl          NUMERIC(14,2) CHECK (cost_brl IS NULL OR cost_brl >= 0),
  sample_collected  BOOLEAN,
  severity_weight   SMALLINT CHECK (severity_weight IN (1,2,4,8)),
  -- FC15: identificação da aeronave
  aircraft_registration TEXT,
  aircraft_type     TEXT,
  engine_type_id    SMALLINT REFERENCES lu_aircraft_engine_type(engine_type_id),
  -- FC15: quase-colisão e alerta a piloto
  near_miss         BOOLEAN DEFAULT FALSE,
  pilot_alerted     BOOLEAN,
  -- FC15: porte estimado quando espécie não identificada
  est_mass_id       SMALLINT REFERENCES lu_mass_class(mass_id),
  est_mass_grams    NUMERIC(10,2) CHECK (est_mass_grams IS NULL OR est_mass_grams > 0),
