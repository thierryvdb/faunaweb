CREATE TABLE IF NOT EXISTS lu_quadrant (
  quadrant_id SMALLINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code TEXT NOT NULL UNIQUE,
  description TEXT,
  grid_row TEXT,
  grid_col SMALLINT,
  latitude_dec NUMERIC(9,6),
  longitude_dec NUMERIC(9,6),
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM lu_quadrant) THEN
    INSERT INTO lu_quadrant (code, description, grid_row, grid_col)
    SELECT CONCAT(letter, col::text),
           CONCAT('Linha ', letter, ', coluna ', col),
           letter,
           col
      FROM unnest(ARRAY['A','B','C','D','E','F','G','H','I','J','K','L','M','N']) AS letter
     CROSS JOIN generate_series(1, 33) AS col;
  END IF;
END $$;

-- Dimensions
CREATE TABLE IF NOT EXISTS dim_location (
  location_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  airport_id  BIGINT NOT NULL REFERENCES airport(airport_id) ON DELETE CASCADE,
  code        TEXT NOT NULL,
  runway_ref  TEXT,
  description TEXT,
  created_at  TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at  TIMESTAMPTZ NOT NULL DEFAULT now()
